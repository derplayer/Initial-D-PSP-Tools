    public static class CRC
    {

        /// <summary>
        /// CRC32 Checksum Calculation
        /// </summary>
        /// <param name="Data">ByteArray</param>
        /// <returns>4 byte CRC Checksum</returns>
        public static List<byte[]> CRC32(byte[] Data)
            {

            List<byte[]> CRC_Collection = new List<byte[]>();

                foreach (UInt32[] crcTable in CrcTables.CRCTableList)
                {
                    UInt32 crc = 0xffffffff;
                    for (int i = 0; i < Data.Length; i++)
                        crc = (crc >> 8) ^ crcTable[(crc & 0xff) ^ Data[i]];
                    crc ^= 0xffffffff;
                    byte[] output = new byte[4];

                    output[0] = (byte)(crc >> 24);
                    output[1] = (byte)(crc >> 16);
                    output[2] = (byte)(crc >> 8);
                    output[3] = (byte)(crc);

                CRC_Collection.Add(output);
                }

            AdlerChecksum testChecksum = new AdlerChecksum();
            if (testChecksum.MakeForBuff(Data))
            {
                CRC_Collection.Add(BitConverter.GetBytes(testChecksum.ChecksumValue));
            }

            return CRC_Collection;
            }
    }

